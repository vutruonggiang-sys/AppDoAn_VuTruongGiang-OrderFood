package com.example.appdoan_vutruonggiang.UI;

public class Key {
    String id,key;
    public Key(){}

    public Key(String id, String key) {
        this.id = id;
        this.key = key;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
